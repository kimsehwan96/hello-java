import java.util.*;
import java.lang.*;
import java.io.*;

class VarTest
{
	public static void main (String[] args) throws java.lang.Exception
	{
		int a;
		a = 1;
		System.out.println(a+1);
		
		a = 2;
		System.out.println(a+1);
		
		double b = 1.1;
		System.out.println(b+1);
		
		String first = "Hello";
		System.out.println(first + " world");
	}
}